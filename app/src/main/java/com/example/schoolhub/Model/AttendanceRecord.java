package com.example.schoolhub.Model;

public class AttendanceRecord {
    private String day;
    private String status;


    public AttendanceRecord(String day, String status) {
        this.day = day;
        this.status = status;
    }

    public String getDay() {
        return day;
    }

    public String getStatus() {
        return status;
    }














}
